    private void tampilkan_data() {
       
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Customer");
        model.addColumn("Nama Customer");
        model.addColumn("Alamat Customer");
        model.addColumn("Telepon Customer");
        model.addColumn("HP Customer");
        model.addColumn("Email Customer");
        
        try {
            String sql = "SELECT * FROM customer";
            java.sql.Connection conn = (Connection)Konfig.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()) {
                model.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),res.getString(6)});
            }
            tbCustomer.setModel(model); 
            
        }catch (SQLException e) {
            System.out.println("Error:" + e.getMessage());
        }
    }
    
