    private void tbCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbCustomerMouseClicked
        int baris = tbCustomer.rowAtPoint(evt.getPoint());
        String IDCustomer = tbCustomer.getValueAt(baris, 0).toString();
        txtId.setText(IDCustomer);
        
        String Nama = tbCustomer.getValueAt(baris, 1).toString();
        txtNama.setText(Nama);
        
        String Alamat = tbCustomer.getValueAt(baris, 2).toString();
        txtAlamat.setText(Alamat);
        
        String Telp = tbCustomer.getValueAt(baris, 3).toString();
        txtTelp.setText(Telp);
        
        String HP = tbCustomer.getValueAt(baris, 4).toString();
        txtHp.setText(HP);
        
        String Email = tbCustomer.getValueAt(baris, 5).toString();
        txtEmail.setText(Email);
        
    }//GEN-LAST:event_tbCustomerMouseClicked

